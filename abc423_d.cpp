typedef long long int ll;
typedef unsigned long long int ull;

template<typename _T>
inline _T fpow(_T a, _T n, _T mod) {
    _T base = a, ret = 1;
    while (n) {
        if (n & 1) {
            ret = ret * base;
            ret %= mod;
        }
        base = base * base;
        base %= mod;
        n >>= 1;
    }
    return ret % mod;
}
//快读函数声明
template< typename Type >
inline Type fread(Type* p = nullptr);

//快速输出函数
template<typename Type>
inline void fwrite(Type x);

struct Customer {
    ll num, arrive, time, id;

    Customer() = default;

    Customer(ll _num, ll _arrive, ll _time, ll _id) :
        num(_num), arrive(_arrive), time(_time), id(_id) {
    }

    bool operator <(const Customer& a)const {
        return arrive > a.arrive;
    }
};

using queData = std::pair<ll, ll>;

Customer c[MAXN];
ll ans[MAXN];
ll n, k, sum;

int main() {

#ifdef _FREOPEN
    freopen("input.txt", "r", stdin);
#endif // _FREOPEN

#ifdef _RUN_TIME
    clock_t start = clock();
#endif // _RUN_TIME

    fread(&n), fread(&k);

    for (size_t i = 0; i < n; i++) {
        ll num, arrive, time;
        fread(&arrive), fread(&time), fread(&num);
        c[i] = Customer(num, arrive, time, i);
    }

    std::priority_queue<queData, std::vector<queData>, std::greater<queData> > que;
    std::vector<ll> ans(n, -1);
    ll sum = 0;
    ll t = 0;
    for (ll i = 0; i < n; i++) {
        t = std::max(t, c[i].arrive);
        while (sum + c[i].num > k) {
            queData q = que.top(); que.pop();
            t = std::max(t, q.first);
            sum -= q.second;
        }
        ans[i] = t;
        que.push({ t + c[i].time, c[i].num });
        sum += c[i].num;
    }
    for (ll i = 0; i < n; i++) {
        printf("%lld\n", ans[i]);
    }



#ifdef _RUN_TIME
    printf("The running duration is not less than %ld ms\n", clock() - start);
#endif // _RUN_TIME
    return 0;
}

template< typename Type >
inline Type fread(Type* p) {
#if _FREAD
    Type ret = 0, sgn = 0, ch = getchar();
    while (!isdigit(ch)) {
        sgn |= ch == '-', ch = getchar();
    }
    while (isdigit(ch)) ret = ret * 10 + ch - '0', ch = getchar();
    if (p != nullptr) {
        *p = Type(sgn ? -ret : ret);
    }
    return sgn ? -ret : ret;
#else
    if (p == nullptr) {
        Type temp;
        p = &temp;
    }
    scanf("%lld", p);
    return *p;


#endif // _FREAD
}


template<typename Type>
inline void fwrite(Type x) {
    int sta[MAX_NUM_SIZE];
    int top = 0;
    do {
        sta[top++] = x % 10, x /= 10;
    } while (x);
    while (top) {
        putchar(sta[--top] + '0');
    }  // 48 是 '0'
    return;
}



