#define READ          false
#define MAX_INF       1e18
#define MAX_NUM_SIZE  35
#define MAXN          (size_t)(1e6+5)
#define MAXM          (size_t)(2e3+5)

typedef long long int ll;
typedef unsigned long long int unill;

//快读函数声明
#if READ
template< typename T >
inline T readf();
#else
template< typename Type >
inline Type readf(Type* p = nullptr);
#endif

//快速输出函数
template<typename Type>
inline void writef(Type x);

ll arr[MAXN], artistMultiset[MAXM];
size_t n, m, ansFirst, ansLast, artistNum;

int main() {
#ifdef _FREOPEN
    freopen("input.txt", "r", stdin);
#endif // _FREOPEN

#ifdef _RUN_TIME
    clock_t start = clock();
#endif // _RUN_TIME

    readf(&n), readf(&m);

    for (size_t i = 1; i <= n; i++) {
        readf(&arr[i]);
    }


    artistMultiset[arr[1]]++;
    artistNum++;
    ansFirst = 1, ansLast = n;
    for (size_t first = 1, last = 1; last <= n; ) {
        if (artistNum == m) {
            if (last- first + 1 < ansLast - ansFirst + 1) {
                ansFirst = first;
                ansLast = last;
            }
            if (!--artistMultiset[arr[first]]) {
                --artistNum;
            }
            ++first;
        }
        else {
            ++last;
            if (!artistMultiset[arr[last]]++) {
                ++artistNum;
            }
        }
    }

    printf("%lld %lld\n", ansFirst, ansLast);


#ifdef _RUN_TIME
    printf("The running duration is not less than %ld ms\n", clock() - start);
#endif // _RUN_TIME
    return 0;
}

