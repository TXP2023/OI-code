typedef long long int ll;
typedef unsigned long long int ull;

template<typename _T>
inline _T fpow(_T a, _T n, _T mod) {
    _T base = a, ret = 1;
    while (n) {
        if (n & 1) {
            ret = ret * base;
            ret %= mod;
        }
        base = base * base;
        base %= mod;
        n >>= 1;
    }
    return ret % mod;
}
//快读函数声明
template< typename Type >
inline Type fread(Type* p = nullptr);

//快速输出函数
template<typename Type>
inline void fwrite(Type x);

struct Task {
    ll val, end;

    Task() = default;

    Task(ll _v, ll _e) : val(_v), end(_e) {}

    bool operator <(const Task& other)const {
        return val > other.val;
    }
};

Task t[MAXN];
ll n;

int main() {

#ifdef _FREOPEN
    freopen("input.txt", "r", stdin);
#endif // _FREOPEN

#ifdef _RUN_TIME
    clock_t start = clock();
#endif // _RUN_TIME

    fread(&n);

    for (size_t i = 1; i <= n; i++) {
        ll val, end;
        fread(&end), fread(&val);
        t[i] = Task(val, end);
    }

    //按照结束时间排序
    std::sort(t + 1, t + 1 + n, [](const Task& a, const Task& b) {
        return a.end < b.end;
    });

    ll tot = 0, ans = 0;
    std::priority_queue<ll> heap;
    for (size_t i = 1; i <= n; i++) {
        if (tot < t[i].end) {
            ++tot;
            ans += t[i].val;
            heap.push(t[i].val);
        }
        else {
            if (heap.top() < t[i].val) {
                ans += t[i].val - heap.top();
                heap.pop();
                heap.push(t[i].val);
            }
        }
    }

    printf("%lld\n", ans);



#ifdef _RUN_TIME
    printf("The running duration is not less than %ld ms\n", clock() - start);
#endif // _RUN_TIME
    return 0;
}

template< typename Type >
inline Type fread(Type* p) {
#if _FREAD
    Type ret = 0, sgn = 0, ch = getchar();
    while (!isdigit(ch)) {
        sgn |= ch == '-', ch = getchar();
    }
    while (isdigit(ch)) ret = ret * 10 + ch - '0', ch = getchar();
    if (p != nullptr) {
        *p = Type(sgn ? -ret : ret);
    }
    return sgn ? -ret : ret;
#else
    if (p == nullptr) {
        Type temp;
        p = &temp;
    }
    scanf("%lld", p);
    return *p;


#endif // _FREAD
}


template<typename Type>
inline void fwrite(Type x) {
    int sta[MAX_NUM_SIZE];
    int top = 0;
    do {
        sta[top++] = x % 10, x /= 10;
    } while (x);
    while (top) {
        putchar(sta[--top] + '0');
    }  // 48 是 '0'
    return;
}



