#define _FREAD        true
#define MAX_INF       1e18
#define MAX_NUM_SIZE  35
#define MAX_COST      (size_t)(1e5+5)

typedef long long int ll;
typedef unsigned long long int ull;

//快读函数声明
template< typename Type >
inline Type fread(Type* p = nullptr);

//快速输出函数
template<typename Type>
inline void fwrite(Type x);

ll dp[MAX_COST];
ll c[5], n;

int main() {

#ifdef _FREOPEN
    freopen("input.txt", "r", stdin);
#endif // _FREOPEN

#ifdef _RUN_TIME
    clock_t start = clock();
#endif // _RUN_TIME

    for (size_t i = 1; i <= 4; i++) {
        fread(&c[i]);
    }

    fread(&n);

    dp[0] = 1;
    for (size_t i = 1; i <= 4; i++) {
        for (size_t j = c[i]; j <= 1e5; j++) {
            dp[j] += dp[j - c[i]];
        }
    }

    while (n--) {
        ll d[5];
        for (size_t i = 1; i <= 4; i++) {
            fread(&d[i]);
        }
        ll cost = fread<ll>(), res = dp[cost];
        for (size_t i = 1; i < (1 << 4); i++) {
            ll temp = cost, cnt = 0;
            for (size_t j = 1; j <= 4; j++) {
                if (i & (1 << (j - 1))) {
                    temp -= (d[j] + 1) * c[j];
                    ++cnt;
                }
            }
            if (temp >= 0) {
                res -= (cnt % 2 * 2 - 1) * dp[temp];
            }
        }
        printf("%lld\n", res);
    }

#ifdef _RUN_TIME
    printf("The running duration is not less than %ld ms\n", clock() - start);
#endif // _RUN_TIME
    return 0;
}

template< typename Type >
inline Type fread(Type* p) {
#if _FREAD
    Type ret = 0, sgn = 0, ch = getchar();
    while (!isdigit(ch)) {
        sgn |= ch == '-', ch = getchar();
    }
    while (isdigit(ch)) ret = ret * 10 + ch - '0', ch = getchar();
    if (p != nullptr) {
        *p = Type(sgn ? -ret : ret);
    }
    return sgn ? -ret : ret;
#else
    if (p == nullptr) {
        Type temp;
        p = &temp;
    }
    scanf("%lld", p);
    return *p;


#endif // _FREAD
}


template<typename Type>
inline void fwrite(Type x) {
    int sta[MAX_NUM_SIZE];
    int top = 0;
    do {
        sta[top++] = x % 10, x /= 10;
    } while (x);
    while (top) {
        putchar(sta[--top] + '0');
    }  // 48 是 '0'
    return;
}



