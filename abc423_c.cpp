#define MAXN          (size_t)(2e5+5)

typedef long long int ll;
typedef unsigned long long int ull;

template<typename _T>
inline _T fpow(_T a, _T n, _T mod) {
    _T base = a, ret = 1;
    while (n) {
        if (n & 1) {
            ret = ret * base;
            ret %= mod;
        }
        base = base * base;
        base %= mod;
        n >>= 1;
    }
    return ret % mod;
}
//快读函数声明
template< typename Type >
inline Type fread(Type* p = nullptr);

//快速输出函数
template<typename Type>
inline void fwrite(Type x);

ll door[MAXN], sum[MAXN];
ll n, pos, ans = 0;

inline ll next(ll pos) {
    pos++;
    if (pos > n) {
        pos -= n;
    }
    return pos;
}

inline ll pre(ll pos) {
    --pos;
    if (pos == 0) {
        pos = n;
    }
    return pos;
}

int main() {

#ifdef _FREOPEN
    freopen("input.txt", "r", stdin);
#endif // _FREOPEN

#ifdef _RUN_TIME
    clock_t start = clock();
#endif // _RUN_TIME

    fread(&n), fread(&pos);

    for (size_t i = 1; i <= n; i++) {
        fread(&door[i]);
        sum[i] = door[i] + sum[i - 1];
    }

    ll l = 0, r = n + 1;
    while (door[l + 1]) {
        ++l;
    }
    while (door[r - 1]) {
        --r;
    }

    if (pos < l) {
        ans += (l - pos) * 2;
    }
    if (pos >= r) {
        ans += (pos - r + 1) * 2;
    }

    ans += (sum[r - 1] - sum[l]) + r - l - 1;
    printf("%lld\n", ans);

#ifdef _RUN_TIME
    printf("The running duration is not less than %ld ms\n", clock() - start);
#endif // _RUN_TIME
    return 0;
}

template< typename Type >
inline Type fread(Type* p) {
#if _FREAD
    Type ret = 0, sgn = 0, ch = getchar();
    while (!isdigit(ch)) {
        sgn |= ch == '-', ch = getchar();
    }
    while (isdigit(ch)) ret = ret * 10 + ch - '0', ch = getchar();
    if (p != nullptr) {
        *p = Type(sgn ? -ret : ret);
    }
    return sgn ? -ret : ret;
#else
    if (p == nullptr) {
        Type temp;
        p = &temp;
    }
    scanf("%lld", p);
    return *p;


#endif // _FREAD
}


template<typename Type>
inline void fwrite(Type x) {
    int sta[MAX_NUM_SIZE];
    int top = 0;
    do {
        sta[top++] = x % 10, x /= 10;
    } while (x);
    while (top) {
        putchar(sta[--top] + '0');
    }  // 48 是 '0'
    return;
}



