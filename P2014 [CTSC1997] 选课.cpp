std::vector<ll> graph[MAXN];
ll dpTable[MAXN][MAXN]; //dpTable[u][i]表示以u为根的子树，总选课数为i情况下的最大值
ll array[MAXN];
ll n, m;

void init(ll _u, ll _value) {
	dpTable[_u][1] = _value;
	for (ll i : graph[_u]) {
		init(i, array[i]);
	}
	return;
}

void fill_dpTable(ll _u) {
    for (ll v : graph[_u]) {
        fill_dpTable(v);
    }

    //前i门子课

    for (ll v : graph[_u]) {
        for (ll j = m; j > 0; --j) {
            for (size_t k = 0; k < j; k++) {
                dpTable[_u][j] = std::max(dpTable[_u][j], dpTable[_u][j - k] + dpTable[v][k]);
            }
        }
    }
}

