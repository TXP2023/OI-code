ll arr[21][21];
ll n;

inline void slove() {
    memset(arr, 0, sizeof(arr));
    arr[1][1] = 1;
    puts("1");
    for (size_t i = 2; i <= n; i++) {
        for (size_t j = 1; j <= i; j++) {
            arr[i][j] = arr[i - 1][j] + arr[i - 1][j - 1];
            printf("%lld ", arr[i][j]);
        }
        puts("");
    }
    return;
}

