#define MAX_NUM_SIZE  35
#define MAXN          1000005
#define MAXM          10005

typedef int ll;
typedef unsigned long long int ull;

//快读函数声明
#if READ
template< typename T >
inline T readf();
#else
template< typename Type >
inline Type readf(Type* p = nullptr);
#endif

//快速输出函数
template<typename Type>
inline void writef(Type x);

struct Edge {
    int u, v;
    int next;
};

int head[MAXN];
Edge edges[MAXN * 2];
bool color[MAXN];
int n, edgeCnt = 0, ans = 0, sum;

inline void addEdge(ll u, ll v) {
    edges[++edgeCnt].u = u;
    edges[edgeCnt].v = v;
    edges[edgeCnt].next = head[u];
    head[u] = edgeCnt;
    return;
}

bool get_dfs(ll u, ll father) {
    bool ret = false;
    for (size_t i = head[u]; i != 0; i = edges[i].next) {
        if (edges[i].v == father) {
            continue;
        }
        ret |= get_dfs(edges[i].v, u);
    }
    if (ret) {
        sum += (color[u] ^ 1);
    }
    return ret | color[u];
}

int main() {
#ifdef _FREOPEN
    freopen("input.txt", "r", stdin);
#endif // _FREOPEN

#ifdef _RUN_TIME
    clock_t start = clock();
#endif // _RUN_TIME

    readf(&n);

    for (size_t i = 1; i <= n; i++) {
        color[i] = readf<ll>();
    }

    for (size_t i = 1; i <= n - 1; i++) {
        ll u = readf<ll>(), v = readf<ll>();
        addEdge(u, v);
        addEdge(v, u);
    }



    for (size_t i = 1; i <= n; i++) {
        if (color[i]) {
            get_dfs(1, 0);
            ans = std::min(sum, ans);
            sum = 0;
        }
    }

    printf("%lld\n", ans);

#ifdef _RUN_TIME
    printf("The running duration is not less than %ld ms\n", clock() - start);
#endif // _RUN_TIME
    return 0;
}

