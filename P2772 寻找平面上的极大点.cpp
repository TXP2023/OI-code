#define _FREAD        true
#define MAX_INF       1e18
#define MAX_NUM_SIZE  35
#define MAXN          (size_t)(5e5+5)

typedef long long int ll;
typedef unsigned long long int ull;

//快读函数声明
template< typename Type >
inline Type readf(Type* p = nullptr);

//快速输出函数
template<typename Type>
inline void writef(Type x);

struct Point {
    ll x, y;
};

Point points[MAXN], res[MAXN];
bool flag[MAXN];
ll n, max = -1, cnt = 0;

int main() {

#ifdef _FREOPEN
    freopen("input.txt", "r", stdin);
#endif // _FREOPEN

#ifdef _RUN_TIME
    clock_t start = clock();
#endif // _RUN_TIME

    readf(&n);

    for (size_t i = 1; i <= n; i++) {
        readf(&points[i].x), readf(&points[i].y);
    }

    std::sort(points + 1, points + 1 + n, [](const Point& a, const Point& b) {
        if (a.x != b.x) {
            return a.x > b.x;
        }
        return a.y > b.y;
    });

    
    for (size_t i = 1; i <= n; i++) {
        if (points[i].y > max) {
            res[++cnt] = points[i];
            max = points[i].y;
        }
    }

    for (size_t i = cnt; i >= 1; i--) {
        printf("(%lld,%lld)", res[i].x, res[i].y);
        if (i != 1) {
            printf(",");
        }
    }

#ifdef _RUN_TIME
    printf("The running duration is not less than %ld ms\n", clock() - start);
#endif // _RUN_TIME
    return 0;
}

template< typename Type >
inline Type readf(Type* p) {
#if _FREAD
    Type ret = 0, sgn = 0, ch = getchar();
    while (!isdigit(ch)) {
        sgn |= ch == '-', ch = getchar();
    }
    while (isdigit(ch)) ret = ret * 10 + ch - '0', ch = getchar();
    if (p != nullptr) {
        *p = Type(sgn ? -ret : ret);
    }
    return sgn ? -ret : ret;
#else
    if (p == nullptr) {
        Type temp;
        p = &temp;
    }
    scanf("%lld", p);
    return *p;


#endif // _FREAD
}


template<typename Type>
inline void writef(Type x) {
    int sta[MAX_NUM_SIZE];
    int top = 0;
    do {
        sta[top++] = x % 10, x /= 10;
    } while (x);
    while (top) putchar(sta[--top] + '0');  // 48 是 '0'
    return;
}



