#define MAX_INF       1e18
#define MAX_NUM_SIZE  35
#define MAXN          (size_t)(2e5+5)

typedef long long int ll;
typedef unsigned long long int ull;

//快读函数声明
template< typename Type >
inline Type readf(Type* p = nullptr);

//快速输出函数
template<typename Type>
inline void writef(Type x);

ll arr[MAXN];
ll t, n, s, ans_l, ans_r;

inline void slove() {
    readf(&n), readf(&s);

    for (size_t i = 1; i <= n; i++) {
        readf(&arr[i]);
    }

    ll sum = arr[1];
    ans_l = 0, ans_r = 0;
    for (size_t l = 1, r = 1; r <= n; ) {
        if (sum >= s) {
            if (r - l + 1 < ans_r - ans_l + 1 || !ans_l) {
                ans_r = r;
                ans_l = l;
            }
            sum -= arr[l];
            ++l;
        }
        else {
            ++r;
            sum += arr[r];
        }
    }
    printf("%lld %lld\n", ans_l, ans_r);
    return;
}

int main() {
#ifdef _FREOPEN
    freopen("input.txt", "r", stdin);
#endif // _FREOPEN

#ifdef _RUN_TIME
    clock_t start = clock();
#endif // _RUN_TIME

    readf(&t);

    while (t--) {
        slove();
    }

#ifdef _RUN_TIME
    printf("The running duration is not less than %ld ms\n", clock() - start);
#endif // _RUN_TIME
    return 0;
}

template< typename Type >
inline Type readf(Type* p) {
    Type ret = 0, sgn = 0, ch = getchar();
    while (!isdigit(ch)) {
        sgn |= ch == '-', ch = getchar();
    }
    while (isdigit(ch)) ret = ret * 10 + ch - '0', ch = getchar();
    if (p != nullptr) {
        *p = Type(sgn ? -ret : ret);
    }
    return sgn ? -ret : ret;
}


template<typename Type>
inline void writef(Type x) {
    int sta[MAX_NUM_SIZE];
    int top = 0;
    do {
        sta[top++] = x % 10, x /= 10;
    } while (x);
    while (top) putchar(sta[--top] + '0');  // 48 是 '0'
    return;
}



