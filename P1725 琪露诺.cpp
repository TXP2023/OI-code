#define MAXN          (size_t)(2e5+5)

typedef long long int ll;
typedef unsigned long long int ull;

template<typename _T>
inline _T fpow(_T a, _T n, _T mod) {
    _T base = a, ret = 1;
    while (n) {
        if (n & 1) {
            ret = ret * base;
            ret %= mod;
        }
        base = base * base;
        base %= mod;
        n >>= 1;
    }
    return ret % mod;
}
//快读函数声明
template< typename Type >
inline Type fread(Type* p = nullptr);

//快速输出函数
template<typename Type>
inline void fwrite(Type x);

struct queData {
    ll pos, w;

    queData() = default;

    queData(ll _pos, ll _w) : pos(_pos), w(_w) {}
};

std::deque<queData> que;
ll dp[MAXN], arr[MAXN];
ll n, l, r, ans = LLONG_MIN, cnt;

int main() {

#ifdef _FREOPEN
    freopen("input.txt", "r", stdin);
#endif // _FREOPEN

#ifdef _RUN_TIME
    clock_t start = clock();
#endif // _RUN_TIME

    fread(&n), fread(&l), fread(&r);

    for (size_t i = 0; i <= n; i++) {
        fread(&arr[i]);
    }

    que.push_back(queData(0, arr[0]));

    for (size_t i = 0; i < l; i++) {
        dp[i] = arr[i];
    }

    cnt = l;
    std::fill(dp, dp + 1 + n, -1e18);
    for (size_t i = l; i <= n; i++) {
        while (!que.empty() && que.front().pos < ll(i - r)) {
            que.pop_front();
        }
        if (!que.empty()) {
            dp[i] = arr[i] + que.front().w;
        }
        if (i - l + 1 < cnt) {
            continue;
        }
        while (!que.empty() && dp[cnt] > que.back().w) {
            que.pop_back();
        }
        que.push_back(queData(cnt, dp[cnt]));
        ++cnt;
    }

    for (size_t i = n - r + 1; i <= n; i++) {
        ans = std::max(ans, dp[i]);
    }

    printf("%lld\n", ans);





#ifdef _RUN_TIME
    printf("The running duration is not less than %ld ms\n", clock() - start);
#endif // _RUN_TIME
    return 0;
}

template< typename Type >
inline Type fread(Type* p) {
#if _FREAD
    Type ret = 0, sgn = 0, ch = getchar();
    while (!isdigit(ch)) {
        sgn |= ch == '-', ch = getchar();
    }
    while (isdigit(ch)) ret = ret * 10 + ch - '0', ch = getchar();
    if (p != nullptr) {
        *p = Type(sgn ? -ret : ret);
    }
    return sgn ? -ret : ret;
#else
    if (p == nullptr) {
        Type temp;
        p = &temp;
    }
    scanf("%lld", p);
    return *p;


#endif // _FREAD
}


template<typename Type>
inline void fwrite(Type x) {
    int sta[MAX_NUM_SIZE];
    int top = 0;
    do {
        sta[top++] = x % 10, x /= 10;
    } while (x);
    while (top) {
        putchar(sta[--top] + '0');
    }  // 48 是 '0'
    return;
}



