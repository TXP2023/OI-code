//ll dp[MAXN][MAXN];
//std::vector<std::vector<ll>> dp;
ll h[MAXN], w[MAXN];
ll n, m, ans = 0;

int main() {

#ifdef _FREOPEN
    freopen("input.txt", "r", stdin);
#endif // _FREOPEN

#ifdef _RUN_TIME
    clock_t start = clock();
#endif // _RUN_TIME

    fread(&n), fread(&m);
    --n, --m;

    if (n == 0 && m == 0) {
        puts("0");
        return 0;
    }

    for (size_t i = 1; i <= n; i++) {
        fread(&h[i]);

    }
    std::sort(h + 1, h + 1 + n, std::greater<ll>());

    for (size_t i = 1; i <= m; i++) {
        fread(&w[i]);
    }
    std::sort(w + 1, w + 1 + m, std::greater<ll>());

    ll cnt = 1, tot = 1;

    size_t i, j;
    for (i = 1, j = 1; i <= n && j <= m; ) {
        if (h[i] >= w[j]) {
            ans += h[i++] * tot;
            ++cnt;
        }
        else {
            ans += w[j++] * cnt;
            ++tot;
        }
    }

    while (i <= n) {
        ans += h[i++] * tot;
        ++cnt;
    }
    while (j <= m) {
        ans += w[j++] * cnt;
        ++tot;
    }


    printf("%lld\n", ans);



#ifdef _RUN_TIME
    printf("The running duration is not less than %ld ms\n", clock() - start);
#endif // _RUN_TIME
    return 0;
}

template< typename Type >
inline Type fread(Type* p) {
#if _FREAD
    Type ret = 0, sgn = 0, ch = getchar();
    while (!isdigit(ch)) {
        sgn |= ch == '-', ch = getchar();
    }
    while (isdigit(ch)) ret = ret * 10 + ch - '0', ch = getchar();
    if (p != nullptr) {
        *p = Type(sgn ? -ret : ret);
    }
    return sgn ? -ret : ret;
#else
    if (p == nullptr) {
        Type temp;
        p = &temp;
    }
    scanf("%lld", p);
    return *p;


#endif // _FREAD
}


template<typename Type>
inline void fwrite(Type x) {
    int sta[MAX_NUM_SIZE];
    int top = 0;
    do {
        sta[top++] = x % 10, x /= 10;
    } while (x);
    while (top) {
        putchar(sta[--top] + '0');
    }  // 48 是 '0'
    return;
}



