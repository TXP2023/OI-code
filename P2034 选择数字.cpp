#define _FREAD        true
#define MAX_INF       1e18
#define MAX_NUM_SIZE  35
#define MAXN          (size_t)(1e5+5)

typedef long long int ll;
typedef unsigned long long int ull;

//快读函数声明
template< typename Type >
inline Type fread(Type* p = nullptr);

//快速输出函数
template<typename Type>
inline void fwrite(Type x);

ll dp[MAXN], arr[MAXN], sum;
ll n, k;

int main() {

#ifdef _FREOPEN
    freopen("input.txt", "r", stdin);
#endif // _FREOPEN

#ifdef _RUN_TIME
    clock_t start = clock();
#endif // _RUN_TIME

    fread(&n), fread(&k);

    for (size_t i = 1; i <= n; i++) {
        fread(&arr[i]);
        sum += arr[i];
    }

    std::deque<ll> que;
    que.push_front(0);
    for (size_t i = 1; i <= n; i++) {
        while (!que.empty() && i - que.front() > k + 1) {
            que.pop_front();
        }
        dp[i] = dp[que.front()] + arr[i];
        while (!que.empty() && dp[i] < dp[que.back()]) {
            que.pop_back();
        }
        que.push_back(i);
    }

    ll minest = MAX_INF;
    for (size_t i = n; i >= n - k; i--) {
        minest = std::min(dp[i], minest);
    }

    printf("%lld\n", sum - minest);


#ifdef _RUN_TIME
    printf("The running duration is not less than %ld ms\n", clock() - start);
#endif // _RUN_TIME
    return 0;
}

template< typename Type >
inline Type fread(Type* p) {
#if _FREAD
    Type ret = 0, sgn = 0, ch = getchar();
    while (!isdigit(ch)) {
        sgn |= ch == '-', ch = getchar();
    }
    while (isdigit(ch)) ret = ret * 10 + ch - '0', ch = getchar();
    if (p != nullptr) {
        *p = Type(sgn ? -ret : ret);
    }
    return sgn ? -ret : ret;
#else
    if (p == nullptr) {
        Type temp;
        p = &temp;
    }
    scanf("%lld", p);
    return *p;


#endif // _FREAD
}


template<typename Type>
inline void fwrite(Type x) {
    int sta[MAX_NUM_SIZE];
    int top = 0;
    do {
        sta[top++] = x % 10, x /= 10;
    } while (x);
    while (top) {
        putchar(sta[--top] + '0');
    }  // 48 是 '0'
    return;
}



