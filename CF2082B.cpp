inline void writef(Type x);

ll t, x, n, m;

ll get_floor(ll _x, ll _y) {
    while (_y--) {
        if (!_x) {
            return 0;
        }
        _x >>= 1;
    }
    return _x;
}

ll get_ceil(ll _x, ll _y) {
    while (_y--) {
        if (_x < 2) {
            return _x;
        }
        _x = _x + 1 >> 1;
    }
    return _x;
}

inline void slove() {
    readf(&x), readf(&n), readf(&m);
    //std::cin >> x >> n >> m;
    printf("%lld %lld\n", get_floor(get_ceil(x, m), n), get_ceil(get_floor(x, n), m));
    return;
}

int main() {

#ifdef _FREOPEN
    freopen("input.txt", "r", stdin);
#endif // _FREOPEN

#ifdef _RUN_TIME
    clock_t start = clock();
#endif // _RUN_TIME


    //std::cin >> t;
    readf(&t);

    while (t--) {
        slove();
    }



#ifdef _RUN_TIME
    printf("The running duration is not less than %ld ms\n", clock() - start);
#endif // _RUN_TIME
    return 0;
}

template< typename Type >
inline Type readf(Type* p) {
#if _FREAD
    Type ret = 0, sgn = 0;
    uint64_t ch = getchar();
    while (!isdigit(ch)) {
        sgn |= ch == '-', ch = getchar();
    }
    while (isdigit(ch)) ret = ret * 10 + ch - '0', ch = getchar();
    if (p != nullptr) {
        *p = Type(sgn ? -ret : ret);
    }
    return sgn ? -ret : ret;
#else
    if (p == nullptr) {
        Type temp;
        p = &temp;
    }
    scanf("%lld", p);
    return *p;


#endif // _FREAD
}


template<typename Type>
inline void writef(Type x) {
    int sta[MAX_NUM_SIZE];
    int top = 0;
    do {
        sta[top++] = x % 10, x /= 10;
    } while (x);
    while (top) putchar(sta[--top] + '0');  // 48 ?? '0'
    return;
}



