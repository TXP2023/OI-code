struct Matrix {
    uint64_t arr_[3][3];

    Matrix() {
        memset(arr_, 0, sizeof(arr_));
    }

    Matrix operator *(Matrix& other) const {
        Matrix ret;
        for (int i = 1; i <= 2; ++i) {
            for (int j = 1; j <= 2; ++j) {
                for (int k = 1; k <= 2; ++k) {
                    ret.arr_[i][j] = (ret.arr_[i][j] + arr_[i][k] * other.arr_[k][j]) % MOD;
                }
            }
        }
        return ret;
    }
};

Matrix ans, base;
ll n;

inline void mul_Self(uint64_t val) {
    while (val) {
        if (val & 1) {
            ans = ans * base;
        } 
        base = base * base;
        val >>= 1;
    }
}

void init() { // 初始化 ans、base 矩阵
    base.arr_[1][1] = base.arr_[1][2] = base.arr_[2][1] = 1;
    ans.arr_[1][1] = ans.arr_[1][2] = 1;
}

//  矩阵初始化如图
//  竖着为第一个索引，横着为第二个
//  base:
//      1  2
//  1 | 1  1 |
//    |      |
//  2 | 1  0 |
//  
//  ans:
//  
//      1  2
//  1 | 1  1 |

int main() {
#ifdef _FREOPEN
    freopen("input.txt", "r", stdin);
#endif // _FREOPEN

#ifdef _RUN_TIME
    clock_t start = clock();
#endif // _RUN_TIME

    readf(&n);

    if (n <= 2) {
        puts("1");
        return 0;
    }

    init();

    mul_Self(n - 2);

    printf("%lld\n", ans.arr_[1][1] % MOD);

#ifdef _RUN_TIME
    printf("The running duration is not less than %ld ms\n", clock() - start);
#endif // _RUN_TIME
    return 0;
}

