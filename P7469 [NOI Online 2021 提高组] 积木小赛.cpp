std::pair<uint64_t, uint64_t> Hash1[MAXN], Hash2[MAXN];
const uint64_t Base = 37951;
const uint64_t Mode = 1937038790342527;
uint64_t ans[MAXN * MAXN];
ll n, cnt;

int main() {
#ifdef _FREOPEN
    freopen("input.txt", "r", stdin);
#endif // _FREOPEN

#ifdef _RUN_TIME
    clock_t start = clock();
#endif // _RUN_TIME

    
    readf(&n);

    for (size_t i = 0; i < n; i++) {
        char ch;
        std::cin >> ch;
        array1[i] = ch - 'a' + 1;
    }

    for (size_t i = 0; i < n; i++) {
        char ch;
        std::cin >> ch;
        array2[i] = ch - 'a' + 1;
    }

    for (size_t first = 0; first < n; first++) {
        ll last = first, Hash = 0;
        for (size_t _Pos = 0; _Pos < n; _Pos++) {
            if (last == n) {
                break;
            }
            if (array2[last] == array1[_Pos]) {
                ++last;
            }
        }
        for (size_t j = first; j < last; j++) {
            Hash = (Hash * Base + array2[j]) % Mode;
            ans[++cnt] = Hash;
        }
    }

    std::sort(ans + 1, ans + cnt + 1);
    printf("%lld\n", std::unique(ans + 1, ans + cnt + 1) - ans - 1);

#ifdef _RUN_TIME
    printf("The running duration is not less than %ld ms\n", clock() - start);
#endif // _RUN_TIME
    return 0;
}

