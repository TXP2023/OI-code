#define MAX_INF       1e18
#define MAX_NUM_SIZE  35
#define MAXN          (size_t)(1e5+5)

typedef long long int ll;
typedef unsigned long long int ull;

//快读函数声明
template< typename Type >
inline Type readf(Type* p = nullptr);

//快速输出函数
template<typename Type>
inline void writef(Type x);

ll pre_sum[MAXN], cnt = 0;
bool prime[MAXN];
ll n;

inline void get_prime() {
    for (size_t i = 2; i <= n; i++) {
        if (prime[i]) {
            continue;
        }
        for (size_t j = 2; j*i <= n; j++) {
            prime[j * i] = true;
        }
    }

    for (size_t i = 1; i <= n; i++) {
        if (!prime[i]) {
            ++cnt;
            pre_sum[cnt] = i + pre_sum[cnt - 1];
        }
    }
    return;
}

inline ll get_sum(ll l, ll r) {
    return pre_sum[r] - pre_sum[l - 1];
}

inline ll get_ans() {
    ll ans = 0;
    for (size_t l = 1, r = 1; l <= r && r <= n; ) {
        if (get_sum(l, r) == n) {
            ++ans;
            ++l;
        }
        if (get_sum(l, r) > n) {
            ++l;
        }
        else {
            ++r;
        }
    }
    return ans;
}

int main() {
#ifdef _FREOPEN
    freopen("input.txt", "r", stdin);
#endif // _FREOPEN

#ifdef _RUN_TIME
    clock_t start = clock();
#endif // _RUN_TIME

    readf(&n);

    get_prime();

    get_ans();

    printf("%lld\n", get_ans());

#ifdef _RUN_TIME
    printf("The running duration is not less than %ld ms\n", clock() - start);
#endif // _RUN_TIME
    return 0;
}

template< typename Type >
inline Type readf(Type* p) {
    Type ret = 0, sgn = 0, ch = getchar();
    while (!isdigit(ch)) {
        sgn |= ch == '-', ch = getchar();
    }
    while (isdigit(ch)) ret = ret * 10 + ch - '0', ch = getchar();
    if (p != nullptr) {
        *p = Type(sgn ? -ret : ret);
    }
    return sgn ? -ret : ret;
}


template<typename Type>
inline void writef(Type x) {
    int sta[MAX_NUM_SIZE];
    int top = 0;
    do {
        sta[top++] = x % 10, x /= 10;
    } while (x);
    while (top) putchar(sta[--top] + '0');  // 48 是 '0'
    return;
}



