template<size_t __MAX_VALUE>
class Log2 {
public:
    Log2();

    int operator ()(const size_t& val) {
        return log_[val];
    }

private:
    int log_[__MAX_VALUE + 1];
};

template<size_t __MAX_VALUE>
Log2<__MAX_VALUE>::Log2() {
    log_[1] = 0;
    log_[1] = 0;
    log_[2] = 1;
    for (size_t i = 3; i <= __MAX_VALUE; i++) {
        log_[i] = log_[i / 2] + 1;
    }
    return;
}

ll arr[MAXN], stTable[MAXN][LOGN];
Log2<MAXN> get_log2;
ll n, m;

//初始化ST表
inline void init() {
    for (size_t i = 1; i <= LOGN; i++) {
        for (size_t j = 1; j + (1 << i) - 1 <= n; j++) {
            stTable[j][i] = std::min(
                stTable[j][i - 1],
                stTable[j + (1 << (i - 1))][i - 1]
            );
        }
    }
    return;
}

int main() {
#ifdef _FREOPEN
    freopen("input.txt", "r", stdin);
#endif // _FREOPEN

#ifdef _RUN_TIME
    clock_t start = clock();
#endif // _RUN_TIME

    readf(&n), readf(&m);

    for (size_t i = 1; i <= n; i++) {
        readf(&stTable[i][0]);
    }

    init();

    for (size_t i = 0; i < m; i++) {
        ll range_begin, range_end;
        readf(&range_begin), readf(&range_end);
        size_t Log = get_log2(range_end - range_begin + 1);
        printf("%lld ", std::min(stTable[range_begin][Log], stTable[range_end - (1 << Log) + 1][Log]));
    }


#ifdef _RUN_TIME
    printf("The running duration is not less than %ld ms\n", clock() - start);
#endif // _RUN_TIME
    return 0;
}

