char str[MAX_STR_LENGTH];
ll strLength, t;

inline void solve() {
    readf(&strLength);
    scanf("%s\n", str + 1);
    if (strLength == 1) {
        putchar(str[1]);
        puts("");
        return;
    }
    str[strLength + 1] = '\0';
    bool flag2 = false;
    for (size_t i = 1; i < strLength; i++) {
        if (str[i] > str[i + 1]) {
            flag2 = true;
            for (size_t j = 1; j < i; j++) {
                putchar(str[j]);
            }
            bool flag = false;
            for (size_t j = i + 1; j < strLength; j++) {
                if (str[i] < str[j]) {
                    flag = true;
                    for (size_t k = i + 1; k < j; k++) {
                        putchar(str[k]);
                    }
                    putchar(str[i]);
                    for (size_t k = j; k <= strLength; k++) {
                        putchar(str[k]);
                    }
                    break;
                }
            }
            if (!flag) {
                for (size_t j = i + 1; j <= strLength; j++) {
                    putchar(str[j]);
                }
                putchar(str[i]);
            }
            break;
        }
    }
    if (!flag2) {
        printf("%s", str + 1);
    }
    puts("");
    return;
}

int main() {
#ifdef _FREOPEN
    freopen("input.txt", "r", stdin);
#endif // _FREOPEN

#ifdef _RUN_TIME
    clock_t start = clock();
#endif // _RUN_TIME

    readf(&t);

    while (t--) {
        solve();
    }


#ifdef _RUN_TIME
    printf("The running duration is not less than %ld ms\n", clock() - start);
#endif // _RUN_TIME
    return 0;
}

